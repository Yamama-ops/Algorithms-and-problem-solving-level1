#include<iostream>
using namespace std;
int main()
{
	int A, B;
	cout << "Enter A" << endl;
	cin >> A;
	cout << "Enter B" << endl;
	cin >> B;
	cout << "\n";
	cout << A << "+" << B << "=" << A + B << endl;
	cout << A << "-" << B << "=" << A - B << endl;
	cout << A << "*" << B << "=" << A * B << endl;
	cout << A << "/" << B << "=" << A / B << endl;


	return 0;
}